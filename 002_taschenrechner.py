# Abfrage der Zahlen
z1 = input("Erste Zahl: ")
z2 = input("Zweite Zahl: ")

# Umwandeln in Zahlen (int)
a = int(z1)
b = int(z2)

# Berechnungen
print("--- Ergebnisse ---")
print("Plus:    " + str(a + b))
print("Minus:   " + str(a - b))
print("Mal:     " + str(a * b))
print("Geteilt: " + str(a / b))